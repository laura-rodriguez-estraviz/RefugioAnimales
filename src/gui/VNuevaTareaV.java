    public void buscarTareasLibres(){
        ModeloTablaTareas mtTareas;
        mtTareas = new ModeloTablaTareas();
        tablaTareasLibres.setModel(mtTareas);
        mtTareas.setFilas(fa.consultarTareasLibres());
        if(mtTareas.getRowCount()>0){
            tablaTareasLibres.setRowSelectionInterval(0,0);
        }else{
            btnAceptar.setEnabled(false);
        }
    }

