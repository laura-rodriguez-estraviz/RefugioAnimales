    private void campoDescripcion(){
        ModeloTablaTareas mtTareas;
        mtTareas = (ModeloTablaTareas)tablaTareas.getModel();
        Descripcion.setText(mtTareas.obtenerTarea(tablaTareas.getSelectedRow()).getDescripcion());
    }
    
    public void buscarTareas(){
        ModeloTablaTareas mtTareas;
        mtTareas = new ModeloTablaTareas();
        tablaTareas.setModel(mtTareas);
        mtTareas.setFilas(fa.consultarTareasVoluntario((Voluntario) this.usuario));
        if(mtTareas.getRowCount()>0){
            tablaTareas.setRowSelectionInterval(0,0);
            btnFichar.setEnabled(true);
            btnCancelar.setEnabled(true);
        }else{
            btnFichar.setEnabled(false);
            btnCancelar.setEnabled(false);
        }
    }

