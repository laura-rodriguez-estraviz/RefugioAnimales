    private void btnFicharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFicharActionPerformed
        if(( (ModeloTablaTareas) tablaTareas.getModel()).getRowCount() < 1){
            btnFichar.setEnabled(false);
            return;
        }
        fa.ficharTarea(( (ModeloTablaTareas) tablaTareas.getModel()).obtenerTarea(tablaTareas.getSelectedRow()), (Voluntario) usuario);
        buscarTareas();
    }//GEN-LAST:event_btnFicharActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(( (ModeloTablaTareas) tablaTareas.getModel()).getRowCount() < 1){
            btnCancelar.setEnabled(false);
            return;
        }
        fa.cancelarTarea(( (ModeloTablaTareas) tablaTareas.getModel()).obtenerTarea(tablaTareas.getSelectedRow()), (Voluntario) usuario);
        buscarTareas();
    }//GEN-LAST:event_btnCancelarActionPerformed

