 public void buscarProductos(){
        ModeloTablaProductosTienda m;
        m=(ModeloTablaProductosTienda) tablaProductos.getModel();
        m.setFilas(fa.obtenerProductosNoTienda( numSucursal,  tienda, nombreText.getText()));
        
        if (m.getRowCount()> 0) {
            tablaProductos.setRowSelectionInterval(0, 0);
        }
    }


