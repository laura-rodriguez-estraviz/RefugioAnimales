    public void ConfigurarInfoSucursalSelecionada(){
        this.nuevo=false;
        borrarbtn.setEnabled(true);
        ModeloTablaSucursales m;
        m=(ModeloTablaSucursales) tablaSucursales.getModel();
        municipioText.setText((String)m.getValueAt(tablaSucursales.getSelectedRow(),1));
        numSucursalText.setText(String.valueOf((Integer) m.getValueAt(tablaSucursales.getSelectedRow(),0)));
}
    
 public void buscarSucursales(){
        ModeloTablaSucursales m;
        m=(ModeloTablaSucursales) tablaSucursales.getModel();
        m.setFilas(fa.obtenerSucursales(municipioBuscarText.getText(), numSucursalBuscarText.getText()));
        
        if (m.getRowCount()> 0) {
            tablaSucursales.setRowSelectionInterval(0, 0);
            ConfigurarInfoSucursalSelecionada();
            guardarbtn.setEnabled(true);
            borrarbtn.setEnabled(true);
        }
        else {
            this.nuevo=false;
            numSucursalText.setText("");
            municipioText.setText("");
            guardarbtn.setEnabled(false);
            borrarbtn.setEnabled(false);
        }
    }
    


