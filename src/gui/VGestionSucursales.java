    private void guardarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarbtnActionPerformed
        // TODO add your handling code here:
        if (nuevo==true){
            fa.nuevaSucursal(Integer.parseInt(numSucursalText.getText()),municipioText.getText());
           
        }
        else{
            ModeloTablaSucursales m;
            m=(ModeloTablaSucursales) tablaSucursales.getModel();
            fa.actualizarSucursal((int)m.getValueAt(tablaSucursales.getSelectedRow(),0),Integer.parseInt(numSucursalText.getText()),municipioText.getText());
        }
         buscarSucursales();
        
    }//GEN-LAST:event_guardarbtnActionPerformed

